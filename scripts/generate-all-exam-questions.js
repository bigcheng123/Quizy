function writeExamQuestions(fileName, units, gradeLabel) {
  const filePath = path.join(BASE, fileName);
  let existing = fs.readFileSync(filePath, 'utf-8');
  const marker = '\n---\n\n## 考试例题';
  existing = existing.split(marker)[0];

  let totalQ = 0;
  for (const u of units) totalQ += (u.match(/<details>/g) || []).length;

  const header = '\n---\n\n## 考试例题\n\n> 以下例题按单元和知识点编排，覆盖全部 ★ 核心考查点。每题标注所属知识点（▸），答案折叠在 <details> 中。\n\n';
  let output = header;
  for (const u of units) output += u;

  fs.writeFileSync(filePath, existing + output, 'utf-8');
  console.log(`${fileName}: ${units.length} units, ${totalQ} questions`);
}

